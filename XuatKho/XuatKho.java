package XuatKho;

public class XuatKho {
    public String MaXuatKho;
    public String TenDienThoai;
    public String SoLuong;
    public String NoiNhan;
    public String NgayXuat;

    public String getMaXuatKho() {
        return MaXuatKho;
    }

    public void setMaXuatKho(String MaXuatKho) {
        this.MaXuatKho = MaXuatKho;
    }

    public String getTenDienThoai() {
        return TenDienThoai;
    }

    public void setTenDienThoai(String TenDienThoai) {
        this.TenDienThoai = TenDienThoai;
    }

    public String getSoLuong() {
        return SoLuong;
    }

    public void setSoLuong(String SoLuong) {
        this.SoLuong = SoLuong;
    }

    public String getNoiNhan() {
        return NoiNhan;
    }

    public void setNoiNhan(String NoiNhan) {
        this.NoiNhan = NoiNhan;
    }

    public String getNgayXuat() {
        return NgayXuat;
    }

    public void setNgayXuat(String NgayXuat) {
        this.NgayXuat = NgayXuat;
    }

    
    
}
